function telephoneCheck(str) {
  
  const regex = {    
  1: /\d{3}-\d{3}-\d{4}/,
  2: /\(\d{3}\)\d{3}-\d{4}/,
  3: /\(\d{3}\) \d{3}-\d{4}/,
  4: /\d{3} \d{3} \d{4}/,
  5: /\d{10}/,
  6: /1 \d{3} \d{3} \d{4}/,
  7: /1 \d{3}-\d{3}-\d{4}/,
  8: /1 \(\d{3}\)\d{3}-\d{4}/,
  9: /1 \(\d{3}\) \d{3}-\d{4}/,
  10: /1 \d{10}/,
  11: /1\d{3} \d{3} \d{4}/,
  12: /1\d{3}-\d{3}-\d{4}/,
  13: /1\(\d{3}\)\d{3}-\d{4}/,
  14: /1\(\d{3}\) \d{3}-\d{4}/,
  15: /1\d{10}/
  }
  
  for (let exp in regex) {
    if (str.match(regex[exp]) == str) {
      return true
    }
  }
  
  return false
}
